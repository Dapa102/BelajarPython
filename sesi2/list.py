# Menampilkan harga barang dalam list
harga_barang = [50000, 30000, 20000]
print(harga_barang[0])
print(harga_barang[1])
print(harga_barang[2])

# Menambahkan item harga barang ke dalam list
harga_barang.append(10000)
print(harga_barang)

# Menampilkan panjang list
print(len(harga_barang))

# Menghapus item harga barang dari list
harga_barang.remove(10000)
print(harga_barang)